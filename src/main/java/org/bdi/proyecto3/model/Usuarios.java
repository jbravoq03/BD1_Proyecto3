package org.bdi.proyecto3.model;

public class Usuarios {

    String Username;

    String Ip;

    public Usuarios() {}

    public String getUsername() {
        return Username;
    }

    public void setUsername(String username) {
        Username = username;
    }

    public String getIp() {
        return Ip;
    }

    public void setIp(String ip) {
        Ip = ip;
    }
}
